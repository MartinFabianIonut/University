package org.example.domain;

public class Pairs {
    private Pair p1,p2;

    public Pairs(Pair p1, Pair p2) {
        this.p1 = p1;
        this.p2 = p2;
    }

    public Pair getP1() {
        return p1;
    }

    public void setP1(Pair p1) {
        this.p1 = p1;
    }

    public Pair getP2() {
        return p2;
    }

    public void setP2(Pair p2) {
        this.p2 = p2;
    }
}
